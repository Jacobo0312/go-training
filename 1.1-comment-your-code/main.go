// The package name is main, which tells the Go compiler that the package should compile as an executable program instead of a shared library.
package main

/*
   The fmt package is one of the standard libraries in Go, and it provides functions for formatting input and output.
*/
import "fmt"

func main() {
	// The Println function from the fmt package is used to print a string to the console.
	fmt.Println("Hello World!")
}
